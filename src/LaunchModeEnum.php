enum LaunchModeEnum: int {

    /** Розробка - все видно всім */
    case Development = 1;

    /** Закритий - сайт недоступний для всіх */
    case Closed = 2;

    /** Альфа - усім видно лише критичні помилки, попередження та нотифікації */
    case Alpha = 3;

    /** Бета - усім видно лише критичні помилки */
    case Beta = 4;

    /** Робочий режим - усім видно лише повідомлення що виникла якась помилка і адміністрацію повідомлено (без відображення деталей) */
    case Production = 5;

    /** Призупинений - доступ лише для адміністрації з повідомленням про технічні роботи для всіх інших */
    case Paused = 6;

}